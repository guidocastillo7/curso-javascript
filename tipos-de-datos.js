// Number - Numérico (OJO JavaScript no diferencia entre tipo flotante y eso, solo tiene tipo number)

// Operador typeof para ver el tipo de dato
console.log(typeof 50)
console.log(typeof 50.67)

// String - Cadena de Texto (También se puede escribir entre comillas simples)
console.log(typeof "Guido Castillo")

// Boolean - true y false
console.log(typeof true)

// Object - Objetos
// Array - Arreglo (Lista): JavaScript toma este dato como object
console.log(typeof ["Guido", "Adolfo", "Mily", "Guibeth"])

// Objeto nativo de JavaScript (Diccionario), también lo toma como object
console.log(typeof {nombre: "Guido", apellido: "Castillo", edad: 28})

// Null - Nulo: También lo toma como object
console.log(typeof null)