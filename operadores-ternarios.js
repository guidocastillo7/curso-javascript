/**
 * Operadores Ternarios: Es para asignar un valor condicionado a una variable
 * Es decir, que dependiendo de la condicion dada se le asigna un valor un otro
 */

let a = 5
let b = 8

/**
 * Se pone el signo de interrogacion como preguntando la condicion
 * Y se escriben los dos valores a asignar dependiendo si la respuesta es True o False
 * Separados por dos puntos ':'
 */ 
let c = b > a ? "ES VERDAD" : "ES FALSO"
console.log(c)