/**
 * Operadores Aritméticos
 * Suma +, Resta -, Multiplicación *, División /, Residuo %, Potencia **
 */

let suma = 50 + 60
let resta = 30 - 10
let multiplica = 2 * 8
let divide = 20 / 4
let residuo = 62 % 4
let potencia = 5**3

// Tambien se pueden operar con variables
let num1 = 50
let num2 = 2

let operacion = num1 / num2

console.log(suma)
console.log(resta)
console.log(multiplica)
console.log(divide)
console.log(residuo)
console.log(operacion)
console.log(potencia)

